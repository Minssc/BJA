#include<iostream>
using namespace std;

int main(){
	cin.tie(0), ios_base::sync_with_stdio(0);
	int T;cin>>T; 
	for(int t=1; t<=T; ++t){
		int A,B;cin>>A>>B; 
		int N = B-A; 
		
		for(long long i=1; ;++i){
			long long pow = i*i; 
			if(pow>=N){
				pow = (i-1)*(i-1); 
				N-=pow; 
				if(N&&N<i)
					cout<<2*(i-1)<<'\n'; 
				else
					cout<<2*(i-1)+1<<'\n'; 
				
				break; 
			}
		}
	}
	
	return 0;
}
